public class TieuChuan {
	private int id_tieuchuan;
	private String tentieuchuan;
	private String noidung_tieuchuan;

	public int getId_tieuchuan() {
		return id_tieuchuan;
	}

	public void setId_tieuchuan(int id_tieuchuan) {
		this.id_tieuchuan = id_tieuchuan;
	}

	public String getTentieuchuan() {
		return tentieuchuan;
	}

	public void setTentieuchuan(String tentieuchuan) {
		this.tentieuchuan = tentieuchuan;
	}

	public String getNoidung_tieuchuan() {
		return noidung_tieuchuan;
	}

	public void setNoidung_tieuchuan(String noidung_tieuchuan) {
		this.noidung_tieuchuan = noidung_tieuchuan;
	}
	

}
