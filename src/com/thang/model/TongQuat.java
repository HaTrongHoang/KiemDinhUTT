package com.thang.model;

public class TongQuat {
	private int id_tongquat;
	private String tieude_tongquat;
	private String noidung_tongquat;

	public int getId_tongquat() {
		return id_tongquat;
	}

	public void setId_tongquat(int id_tongquat) {
		this.id_tongquat = id_tongquat;
	}

	public String getTieude_tongquat() {
		return tieude_tongquat;
	}

	public void setTieude_tongquat(String tieude_tongquat) {
		this.tieude_tongquat = tieude_tongquat;
	}

	public String getNoidung_tongquat() {
		return noidung_tongquat;
	}

	public void setNoidung_tongquat(String noidung_tongquat) {
		this.noidung_tongquat = noidung_tongquat;
	}

}
