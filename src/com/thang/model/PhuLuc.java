package com.thang.model;

public class PhuLuc {
	private int id_phuluc;
	private String tenphuluc;
	private String tieude_phuluc;
	private String noidung_phuluc;

	public int getId_phuluc() {
		return id_phuluc;
	}

	public void setId_phuluc(int id_phuluc) {
		this.id_phuluc = id_phuluc;
	}

	public String getTenphuluc() {
		return tenphuluc;
	}

	public void setTenphuluc(String tenphuluc) {
		this.tenphuluc = tenphuluc;
	}

	public String getTieude_phuluc() {
		return tieude_phuluc;
	}

	public void setTieude_phuluc(String tieude_phuluc) {
		this.tieude_phuluc = tieude_phuluc;
	}

	public String getNoidung_phuluc() {
		return noidung_phuluc;
	}

	public void setNoidung_phuluc(String noidung_phuluc) {
		this.noidung_phuluc = noidung_phuluc;
	}

}
